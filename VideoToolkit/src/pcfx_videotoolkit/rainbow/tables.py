#: The AC codes do not fill their 12-bit space: prefixes 0xF80..0xFFF are
#: unreachable in valid data. Mednafen's generated tables park those on
#: `(0x00, 0x05)`, which is reproduced here so a malformed stream decodes the
#: same way it would in the emulator.
AC_LUT_FALLBACK = (0x00, 0x05)


def build_decode_lut(
    base: tuple[int, ...],
    codes: tuple[int, ...],
    minimum: tuple[int, ...],
    maximum: tuple[int, ...],
    bits: int,
    fallback: tuple[int, int] = (0, 0),
) -> tuple[tuple[int, int], ...]:
    """Expand a compact code definition into a `1 << bits` quick-lookup table."""
    lut: list[tuple[int, int]] = [fallback] * (1 << bits)
    for bit_count in range(2, bits + 1):
        if maximum[bit_count] == 0xFFFF:
            continue
        shift = bits - bit_count
        for code in range(minimum[bit_count], maximum[bit_count] + 1):
            value = codes[base[bit_count] + (code - minimum[bit_count])]
            prefix = code << shift
            for suffix in range(1 << shift):
                lut[prefix | suffix] = (value, bit_count)
    return tuple(lut)


def build_encode_table(
    base: tuple[int, ...],
    codes: tuple[int, ...],
    minimum: tuple[int, ...],
    maximum: tuple[int, ...],
) -> dict[int, tuple[int, int]]:
    """Inverse mapping: symbol -> (code, bit_count). Used by the encoder."""
    table: dict[int, tuple[int, int]] = {}
    for bit_count in range(2, 13):
        if maximum[bit_count] == 0xFFFF:
            continue
        for code in range(minimum[bit_count], maximum[bit_count] + 1):
            value = codes[base[bit_count] + (code - minimum[bit_count])]
            table.setdefault(value, (code, bit_count))
    return table


